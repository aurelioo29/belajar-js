// ada 3 jenis pembulatan
// 1) pembulatan keatas itu Math.ceil
let bilangan1 = 2;
let bilangan2 = 5;
console.log(Math.ceil(bilangan1 / bilangan2));
// 2) pembulatan standar itu Math.round
console.log(Math.round(bilangan1 / bilangan2));
// 3) pembulatan kebawah itu Math.floor
console.log(Math.floor(bilangan1 / bilangan2));
